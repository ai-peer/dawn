for fp in missing_entry_point:
  with open(fp, 'r+', newline='\n') as f:
    has_entry_point = False

    # Collect all functions we need to call from our new entry point
    # For now, just assume we need to call all of them, even if some are transitively called.
    fn_calls = []

    lines = f.readlines()
    for i in range(len(lines)):
      line = lines[i]

      # If file already contains an entry point, skip it
      if '@compute' in line or '@vertex' in line or '@fragment' in line:
        has_entry_point = True
        break

      if line.startswith('fn'):
        call_args_init_exprs = []
        args = re.search(r'\s*fn\s+(.*?)\s*\((.*?)\).*', line)
        fn_name = args.groups()[0]
        has_args = args != None and len(args.groups()[1]) > 1
        if has_args:
          # Some gross code to split args by comma, but taking into account that there
          # may be commas inside types, e.g. "ptr<workgroup, i32>". We do this naively
          # by only splitting on the very last comma found before each ':'.
          args_string = args.groups()[1]
          last_split_index = 0
          comma_index = -1
          split_args = []
          for c in range(len(args_string)):
            if args_string[c] == ',':
              comma_index = c
            elif args_string[c] == ':':
              if comma_index != -1:
                split_args.append(args_string[last_split_index:comma_index].strip())
                last_split_index = comma_index + 1
                comma_index = -1
            elif c+1 == len(args_string):
                split_args.append(args_string[last_split_index:].strip())
          
        #   print(f'last function defined in {fp} with args: {line}, split_args: {split_args}')
          call_args_init_exprs = [a.split(':')[1].strip() + "()" for a in split_args]

        # Add call to function with default type init expressions for each arg (if any)
        fn_calls.append(f'{fn_name}({", ".join(call_args_init_exprs)});')

    if not has_entry_point:
      lines.append('\n')
      lines.append('@compute @workgroup_size(1)\n')
      lines.append('fn generated_entry_point() {\n')
      for fn_call in fn_calls:
        lines.append(f'  {fn_call}\n')
      lines.append('}\n')

      f.seek(0)
      f.writelines(lines)
